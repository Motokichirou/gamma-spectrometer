static float    shape[PULSEGEN_TABLE_LEN];
static uint32_t mean_period;
static uint16_t baseline;
static uint32_t rng_state = 0xC0FFEE42u;

static uint32_t rng(void)
{
    rng_state = rng_state * 1664525u + 1013904223u;
    return rng_state;
}

/* U(0,1), без нулей (для логарифма) */
static float rng_uf(void)
{
    return ((float)((rng() >> 8) + 1u)) / 16777217.0f;  /* 2^24 */
}

void pulsegen_init(uint32_t mean_period_us, uint16_t dac_baseline)
{
    mean_period = (mean_period_us > 0) ? mean_period_us : 1u;
    baseline    = dac_baseline;

    const float tp = 8.0f;
    for (uint32_t k = 0; k < PULSEGEN_TABLE_LEN; k++) {
        float x = (float)k / tp;
        shape[k] = x * x * expf(2.0f * (1.0f - x));
    }
    shape[PULSEGEN_TABLE_LEN - 1] = 0.0f;
}

static uint16_t next_amplitude(void)
{
    /* выборка из реального распределения: индекс + линейная интерполяция */
    uint32_t r = rng();
    uint32_t idx = (r >> 16) % (INVCDF_LEN - 1u);
    uint32_t frac = r & 0xFFFFu;
    uint32_t a = invcdf[idx];
    uint32_t b = invcdf[idx + 1u];
    return (uint16_t)(a + (((b - a) * frac) >> 16));
}

uint16_t pulsegen_fill(uint32_t *table)
{
    uint16_t amp = next_amplitude();
    for (uint32_t k = 0; k < PULSEGEN_TABLE_LEN; k++) {
        uint32_t v = baseline + (uint32_t)(shape[k] * (float)amp + 0.5f);
        table[k] = (v > 4095u) ? 4095u : v;
    }
    return amp;
}

uint32_t pulsegen_next_delay_us(void)
{
    float d = -(float)mean_period * logf(rng_uf());
    if (d < 60.0f) d = 60.0f;         /* импульс 48 мкс + возврат к baseline */
    if (d > 60000.0f) d = 60000.0f;
    return (uint32_t)d;
}
